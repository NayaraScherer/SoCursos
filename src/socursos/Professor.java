package socursos;

public class Professor {
    public String nomeProf;
    public String emailProf;
    public String cursosMinistrados;

    public String getNomeProf() {
        return nomeProf;
    }

    public void setNomeProf(String nomeProf) {
        this.nomeProf = nomeProf;
    }

    public String getEmailProf() {
        return emailProf;
    }

    public void setEmailProf(String emailProf) {
        this.emailProf = emailProf;
    }

    public String getCursosMinistrados() {
        return cursosMinistrados;
    }

    public void setCursosMinistrados(String cursosMinistrados) {
        this.cursosMinistrados = cursosMinistrados;
    }
}
